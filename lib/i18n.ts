// Get translation with fallback to English
export function t(locale: Locale, key: string, params?: Record<string, string>): string {
  let text = translations[locale]?.[key] || translations.en[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
}

// Get/set locale from localStorage
export function getLocale(): Locale {
  if (typeof window === "undefined") return DEFAULT_LOCALE;
  const saved = localStorage.getItem("locale");
  if (saved && LANGUAGES.some((l) => l.code === saved)) return saved as Locale;
  // Auto-detect from browser
  const browserLang = navigator.language;
  if (browserLang.startsWith("zh-TW") || browserLang.startsWith("zh-Hant")) return "zh-TW";
  if (browserLang.startsWith("zh")) return "zh-CN";
  const match = LANGUAGES.find((l) => browserLang.startsWith(l.code.split("-")[0]));
  return match?.code || DEFAULT_LOCALE;
}

export function setLocale(locale: Locale) {
  localStorage.setItem("locale", locale);
}
